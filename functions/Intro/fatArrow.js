// write a program that prints out if two people love each other
function theyLoveEachotherRegular (pearsonA, pearsonB) {
  console.log(`${pearsonA} and ${pearsonB} are in love!`);
}
theyLoveEachotherRegular('Joe', 'John'); // Joe and John are in love!


// fat arrow syntax
const theyLoveEachOtherFatArrow = (pearsonA, pearsonB) => {
  console.log(`${pearsonA} and ${pearsonB} are in love!`);
}
theyLoveEachOtherFatArrow('Bernice', 'Samantha'); // Bernice and Samantha are in love!
